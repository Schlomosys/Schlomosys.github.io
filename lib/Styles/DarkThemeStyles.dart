import 'dart:ui';

import 'package:flutter/material.dart';

class Styles {

  static ThemeData themeData(bool isDarkTheme, BuildContext context) {
    return ThemeData(
      primarySwatch:  isDarkTheme ?Colors.purple:Colors.red,
      primaryColor: isDarkTheme ?  Color.fromRGBO(18, 18, 18, 1.0):Color.fromRGBO(254,234,230, 1.0),

      backgroundColor: isDarkTheme ? Color.fromRGBO(33, 33, 33, 1.0):Color.fromRGBO(254,219,208, 1.0),


      indicatorColor: isDarkTheme ? Color(0xff0E1D36) : Color(0xffCBDCF8),
      buttonColor: isDarkTheme ? Color(0xff3B3B3B) : Color(0xffF1F5FB),

      hintColor: isDarkTheme ? Color(0xff280C0B) : Color(0xffEECED3),

      highlightColor: isDarkTheme ? Color(0xff372901) : Color(0xffFCE192),
      hoverColor: isDarkTheme ? Color(0xff3A3A3B) : Color(0xff4285F4),

      focusColor: isDarkTheme ? Color(0xff0B2512) : Color(0xffA8DAB5),
      disabledColor: Colors.grey,
      //textButtonTheme: isDarkTheme? Color.fromRGBO(48, 48, 48, 0.75):Color.fromRGBO(254,234,230, 0.85),
      textSelectionColor: isDarkTheme ? Colors.white : Colors.black,
      cardColor: isDarkTheme ? Color(0xFF151515) : Colors.white,
      canvasColor: isDarkTheme ? Color.fromRGBO(48, 48, 48, 0.75):Color.fromRGBO(254,234,230, 0.85),
      brightness: isDarkTheme ? Brightness.dark : Brightness.light,
      buttonTheme: Theme.of(context).buttonTheme.copyWith(
          colorScheme: isDarkTheme ? ColorScheme.dark() : ColorScheme.light()),
      appBarTheme: AppBarTheme(
        elevation: 10.0,shadowColor: Color.fromRGBO(244, 143, 177, 1.0),
        brightness:isDarkTheme ?Brightness.dark : Brightness.light,

      ),
    );

  }
}